class ServiceInformation {
  final DateTime availed_date;
  final int special_event;
  final DateTime scheduled_date;
  final String service;
  final String event_type;
  final String fullName;
  final String skk_number;
  final String address;
  final String landmark;
  final String contact_number;
  final String select_type;

  ServiceInformation({
    required this.availed_date,
    required this.special_event,
    required this.scheduled_date,
    required this.service,
    required this.event_type,
    required this.fullName,
    required this.skk_number,
    required this.address,
    required this.landmark,
    required this.contact_number,
    required this.select_type,
  });
}



